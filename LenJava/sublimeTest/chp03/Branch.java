public class Branch{
	public static void main(String[] args) {
		int a=12;

		switch(a){
		case 10:
			System.out.println("er");
			break;
		case 11:
			System.out.println("er");
			break;
		case 12:
			System.out.println("su");
			break;
		}
		/**
		 * 注意switch语句的判断条件不能是小数
		 */
		
		// if(a=1){System.out.println("if");}//注意if只能判断布尔值
	}
}